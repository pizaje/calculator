function add(num1, num2) {
  return num1 + num2;
}

function sub(num1, num2) {
  return num1 - num2;
}

function mult(num1, num2) {
  return num1 * num2;
}

function divide(num1, num2) {
  return num1 / num2;
}

console.log(add(4, 5));
console.log(sub(4, 5));
console.log(mult(4, 5));
console.log(divide(4, 5));